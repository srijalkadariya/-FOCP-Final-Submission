'''
Over the summer, temperatures in Yorkshire reached 38.4C. Write a program that converts this value in Celsius to the equivalent temperature in Fahrenheit, and then displays both.
'''
temparature_celcius=38.4
temparature_fahrenheit=(temparature_celcius * 9/5)+32
print(f"The temprature in celcius is {temparature_celcius}C and in fahrenheit is {temparature_fahrenheit}F")